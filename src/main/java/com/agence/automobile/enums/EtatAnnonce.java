package com.agence.automobile.enums;

/**
 * États possibles pour une annonce de voiture.
 */
public enum EtatAnnonce {
    /** L'annonce est publiée et disponible. */
    ACTIVE,
    /** L'annonce est désactivée et ne doit plus être proposée. */
    DESACTIVE,
    /** L'annonce a été vendue. */
    VENDUE
}
